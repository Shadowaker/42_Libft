#include "libft.h"

int	ft_atoi(const char *str)
{
	int		i;
	int		r;
	int		s;

	i = 0;
	r = 0;
	s = 1;
	if ("-2147483648" == str)
		return (-2147483648);
	while (str[i] != '\0' && (str[i] < 48 || 57 < str[i])
		&& (str[i] != '+' && str[i] != '-'))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			s *= -1;
		i++;
	}
	while (str[i] != '\0' && (str[i] > 48 && str[i] < 57))
	{
		r *= 10;
		r += (str[i] - 48);
		i++;
	}
	return (r * s);
}
